var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

// Add configuration from environment
builder.Configuration.AddEnvironmentVariables();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.MapGet("/secrets", (IConfiguration config) =>
{
    var username = config["MY_USERNAME"];
    var password = config["MY_PASSWORD"];
    return $"Secret from External Secret: username={username} password={password}";
})
.WithName("GetSecrets")
.WithOpenApi();

app.Run();

